	<h2 id="ecobaladeur" >EcoBaladeur</h2>
	<ul>
		<li>
			<h3>Où puis-je télécharger l’application ?</h3>
				<ul>
					<li><p>Les applications Iphone et Android portent toutes les deux le nom d’ecoBalade et vous pouvez les télécharger gratuitement depuis le « Store » dédié de votre appareil.</p></li>
				</ul>
		</li>
		<li>
		<h3>Est-ce que je peux créer mon parcours ?</h3>
			<ul>
				<li><p>Pas encore. Mais rassurez-vous, de nouveaux parcours apparaissent régulièrement. Vos balades préférées y figureront peut-être prochainement !</p></li>
			</ul>
		</li>
		<li>
			<h3>Pourquoi n’y a t-il pas d’ecoBalade près de chez moi ?</h3>
			<ul>
				<li><p>Parce que nous travaillons avec vos mairies ou votre région pour qu’elles puissent vous les offrir. N’hésitez pas à leur faire savoir !</p></li>
			</ul>
		</li>
		<li>
		<h3>Est-ce que je peux être accompagné ?</h3>
			<ul>
				<li><p>Bientôt, des animateurs naturalistes pourront vous accompagner lors de vos balades.</p></li>
				<li><p>EcoBalade met en lumière les rapports humains en donnant envie de rencontrer un guide dont la passion contagieuse fait la garantie de la richesse de cette expérience.</p></li>
				<li><p>L’animateur naturaliste proposera un programme, des dates et horaires d’ecoBalade. Il disposera du nombre maximum de participants. Vous pourrez vous inscrire directement en ligne.</p></li>
			</ul>
		</li>
		<li>
		<h3>Est-ce que je peux utiliser ecoBalade en dehors des parcours proposés ?</h3>
			<ul>
				<li><p>Bien sûr ! Les clés de découvertes sont réalisées à partir des espèces présentes sur les balades. Mais, vous retrouverez sûrement bon nombre de ces espèces sur d’autres promenades. Avec ecoBalade vous avez toujours votre clé en poche pour identifier les espèces que vous découvrez !</p></li>
			</ul>
		</li>
		<li>
		<h3>Faut-il être un expert de la nature pour faire une ecoBalade ?</h3>
			<ul>
				<li><p>Non, nous avons créé cet outil pour vous initier à l’observation de la nature. La clé de découverte vous permet d’identifier les espèces à la manière d’un naturaliste. Laissez-vous prendre au jeu, et vous deviendrez l’expert de vos sentiers.</p></li>
			</ul>
		</li>
		<li>
		<h3>Est-ce que je peux faire des photos ?</h3>
			<ul>
				<li><p>Bien sûr. Il existe au moins une bonne raison pour prendre des photos. Si vous n’arrivez pas à reconnaitre une espèce, même avec la clé, alors photographiez là, envoyez-là sur le site et nous vous dirons de quelle espèce il s’agit.</p></li>
			</ul>
		</li>
	</ul>	
