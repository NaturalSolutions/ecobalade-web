	<h2 >Enseignants</h2>
	<ul>
		<li>
		<h3>Est-ce que je peux moi-même animer une balade ?</h3>
			<ul>
			<li><p>Oui, le service ecoBalade est spécialement conçu pour favoriser la médiation des enseignants envers les élèves. Prochainement, nous allons proposer avec des instances éducatives de votre région un protocole d’initiation à l’ecoBalade, utilisant les tablettes numériques. Ce protocole vous présentera le scénario et les supports mis en œuvre pour animer une ecoBalade dans une démarche d’investigation.</p></li>
			<li><p>Pour connaitre l’avancement de ce projet, je vous invite à nous contacter au 04 91 33 53 87.</p></li>
			</ul>
		</li>
		<li>
		<h3>De quoi disposerai-je pour animer l’ecoBalade ?</h3>
			<ul>
				<li><p>En concertation avec le Rectorat, nous avons opté pour une utilisation du service sur une tablette numérique dont l’intérêt pédagogique est aujourd’hui largement démontré. Moins personnel que le Smartphone, on peut créer un petit groupe de 4 ou 5 écoliers autour d’une tablette numérique, améliorant l’interactivité dans l’animation. La prise en main est très rapide et l’autonomie importante.</p></li>
				<li><p>Pour vous procurer des tablettes numériques, vous pouvez contacter votre académie, votre rectorat et/ou vos collectivités territoriales. </p></li>
			</ul>
		</li>
		<li>
		<h3>A quelle classe s’adressera ecoBalade ?</h3>
			<ul>
				<li><p>L’initiation à la biodiversité et aux <abbr title="Technologies de l'information et de la communication pour l'enseignement">TICE</abbr> grâce au service ecoBalade est à l’heure actuelle pensée pour le cycle 3 des écoles élémentaires (CE2, CM1, CM2).</p></li>
			</ul>
		</li>
	</ul>
